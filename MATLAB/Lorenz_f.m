%-------------------------------------------------------------------------
%  Lorenz system: dx/dt = f(x)
%            state:  x(1)==X, x(2)==Y, x(3)==Z.
%       parameters:  p(1)==s, p(2)==r, p(3)==b  (classic: s=10,r=28,b=8/3)
%-------------------------------------------------------------------------
 function dx = Lorenz_f(x)
    global p
    dx = zeros(size(x)) ;
    dx(1) = p(1)*(-x(1)+x(2)) ;
    dx(2) = x(1)*(-x(3)+p(2)) - x(2) ;
    dx(3) = x(1)*x(2) - p(3)*x(3) ;
 end


