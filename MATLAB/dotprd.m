%-------------------------------------------------------------------------
% dot product.  n_==-1 is a flag to exclude parameter T.
%-------------------------------------------------------------------------
 function d = dotprd(n_,a,b)
   n1 = 1 ;
   if n_==-1
      n1 = 2 ;
   end
   d = sum(a(n1:end).*b(n1:end)) ;
 end


